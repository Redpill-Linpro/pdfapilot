package org.redpill.pdfapilot.server.domain;

public class Version {

  private String _version;

  private Long _time;

  public String getVersion() {
    return _version;
  }

  public void setVersion(String version) {
    _version = version;
  }

  public Long getTime() {
    return _time;
  }

  public void setTime(Long total) {
    _time = total;
  }

}
